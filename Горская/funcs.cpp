#include <iostream>
#include <cmath>    // Подключаем библиотеку для математических функций

using namespace std;

int main() {
    // Определим переменную для примеров (например, 0.5, что удобно для тригонометрических функций)
    double x = 0.5;
    
    cout << "Основные математические функции в C++:" << endl << endl;
    
    // 1. Синус (sin) – синус угла, измеряется в радианах:
    cout << "Синус (sin): sin(" << x << ") = " << sin(x) << endl;
    
    // 2. Косинус (cos) – косинус угла:
    cout << "Косинус (cos): cos(" << x << ") = " << cos(x) << endl;
    
    // 3. Тангенс (tan) – тангенс угла:
    cout << "Тангенс (tan): tan(" << x << ") = " << tan(x) << endl;
    
    // 4. Арксинус (asin) – обратная функция к синусу:
    cout << "Арксинус (asin): asin(" << sin(x) << ") = " << asin(sin(x)) << endl;
    
    // 5. Арккосинус (acos) – обратная функция к косинусу:
    cout << "Арккосинус (acos): acos(" << cos(x) << ") = " << acos(cos(x)) << endl;
    
    // 6. Арктангенс (atan) – обратная функция к тангенсу:
    cout << "Арктангенс (atan): atan(" << tan(x) << ") = " << atan(tan(x)) << endl;
    
    // 7. Гиперболический синус (sinh):
    cout << "Гиперболический синус (sinh): sinh(" << x << ") = " << sinh(x) << endl;
    
    // 8. Гиперболический косинус (cosh):
    cout << "Гиперболический косинус (cosh): cosh(" << x << ") = " << cosh(x) << endl;
    
    // 9. Гиперболический тангенс (tanh):
    cout << "Гиперболический тангенс (tanh): tanh(" << x << ") = " << tanh(x) << endl;
    
    // 10. Экспонента (exp) – вычисление e^x:
    cout << "Экспонента (exp): exp(" << x << ") = " << exp(x) << endl;
    
    // 11. Натуральный логарифм (log) – логарифм по основанию e:
    cout << "Натуральный логарифм (log): log(" << x << ") = " << log(x) << endl;
    
    // 12. Логарифм по основанию 10 (log10):
    cout << "Логарифм по основанию 10 (log10): log10(" << x << ") = " << log10(x) << endl;
    
    // 13. Квадратный корень (sqrt):
    cout << "Квадратный корень (sqrt): sqrt(" << x << ") = " << sqrt(x) << endl;
    
    // 14. Возведение в степень (pow) – например, x^3:
    cout << "Возведение в степень (pow): pow(" << x << ", 3) = " << pow(x, 3.0) << endl;
    
    // 15. Абсолютное значение (fabs):
    double y = -x; // зададим отрицательное число для примера
    cout << "Абсолютное значение (fabs): fabs(" << y << ") = " << fabs(y) << endl;
    
    // 16. Округление вверх (ceil) – округление до ближайшего большего целого:
    double z = 1.3;
    cout << "Округление вверх (ceil): ceil(" << z << ") = " << ceil(z) << endl;
    
    // 17. Округление вниз (floor) – округление до ближайшего меньшего целого:
    cout << "Округление вниз (floor): floor(" << z << ") = " << floor(z) << endl;
    
    // 18. Остаток от деления (fmod) – для вещественных чисел:
    double a = 5.5, b = 2.0;
    cout << "Остаток от деления (fmod): fmod(" << a << ", " << b << ") = " << fmod(a, b) << endl;
    
    return 0;
}

